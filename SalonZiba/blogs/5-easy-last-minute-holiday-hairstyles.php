        <h4 class="servicesTitle">Upgraded fish braid</h4>
        
        <br>
        
        <p>We’re all fans of the simple fish braid, but what if we tell you there’s a simple way to make it look even better? Start by using a hair oil to add shine and protect your hair when straightening it. Take a small strand from the top of your head and start creating a fine fish braid. After a while, take two strands from the side of your head and work them into the braid. Do this as many times as you need. Finally, secure your hair with a small elastic band.</p>
        
