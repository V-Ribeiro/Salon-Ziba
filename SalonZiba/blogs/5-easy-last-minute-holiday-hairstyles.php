        <h4 class="servicesTitle">Loose Braids</h4>
        
        <br>
        
        <p>You don’t need a ton of skill to achieve this amazing hairstyle! Start by adding a heat protectant to your locks, curl them and comb through with a wide comb. Once your locks are cooled down, you simply french braid the top sections from both sides of your head until they meet in the middle. Use a thin elastic band to secure the loose braids, add some hair accessories if you like and your holiday hairstyle is ready!</p>
        
        
        <br class="invisivelMovel">
        <br>
        <br>
        
        <img src="../images/blogs/5easyLastMinute/longbraid.png" class="blogImage" align="left" style="margin-right:5%;" alt="twisted braid hair tutorial mosaic">
        
        <br class="invisivelDesktop">
        
        <h4 class="servicesTitle">Twisted Look</h4>
        
        <br>
        
        <p>We love this beautiful twisted braid! It’s the perfect hairstyle for every occasion and done within minutes. Start by selecting two strands from the top section of your head and do a simple knot. After this, continue repeating these knots with lower strands, until the braid reaches your neck. Wrap a hairband around the braid to secure it and hide your ponytail with the help of some bobby pins under the knots you created. This was easy, right?!</p>
        
        <br class="invisivelMovel">
