        <h4 class="servicesTitle">Upside Down</h4>
        
        <br>
        
        <p>This stunning holiday look is way easier to create than it might look! Brush your hair well, turn your head upside down and start french braiding it from your neck. Once the braided part reaches the point you normally wear a high bun, it’s time to secure your locks with an elastic hair band. The only thing left to do now is to wrap your hair into a bun and secure the look with some bobby pins and hairspray. Done!</p>
        
        
        <br class="invisivelMovel">
