        <br>
        
        <p>Before blow drying or using other hot styling tools, you should always apply a heat protectant to your locks in order to avoid damage. Most leave-in conditioners offer this important heat protectant and provide an extra layer of protection when using heat to style your hair. The deep repairing and moisturizing product will help you keep your locks healthy and fight against frizz and split ends!</p>
        
