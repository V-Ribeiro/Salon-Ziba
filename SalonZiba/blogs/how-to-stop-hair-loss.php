        <h4 class="servicesTitle">Change your diet</h4>
        
        <p>There are several vitamins and foods that can promote hair growth and let your hair grow stronger. A nutrient-rich and health-conscious diet will definitely help you reduce hair loss by covering essential basics. Concentrate on eating lean meats, fish, soy, other high protein foods and products rich in Vitamin A, Vitamin E and Vitamin B.  Vitamin A encourages a healthy production of your scalps sebum, Vitamin E promotes better blood circulation and Vitamin B helps to maintain your hair’s health. Give it a try!</p>
        
