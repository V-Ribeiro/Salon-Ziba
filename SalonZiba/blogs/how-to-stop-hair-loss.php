        <h4 class="servicesTitle">Scalp Care</h4>
        
        <br>
        
        <p>Brushing your hair correctly is essential when trying to reduce hair loss. Keep in mind to always brush your hair when it’s dry. Wet hair is in its weakest state and shouldn’t be brushed as it increases hair loss significantly. If you really need to brush your hair wet, use a wide-toothed comb in order to prevent damage. In general, pay attention to how often you brush your hair. Brushing it too often can injure your hair and support hair loss. Try to detangle your hair with your fingers instead!</p>
        
        
