        <h4 class="servicesTitle">How do I use Olaplex at home?</h4>
        
        <br>
        
        <p>At home, you will be using Olaplex 3, designed to provide ongoing protection to your hair. No. 3 Hair Perfector is not a conditioner and is not meant to be left in your hair, you must remove it with shampoo.
        
        <br>
        <br>

Olaplex 3 is best applied to towel-dried hair. If there are any remaining oils or silicone-based products left in your hair, it is recommended to shampoo and towel dry your hair before starting the process.

		<br>
        <br>

Apply a generous amount and comb it through your hair. Leave it for 10 to 20 minutes or more for maximum efficiency. Afterwards, rinse the product off, then apply shampoo and conditioner as usual. For healthy hair, it can be used once a week, for damaged hair 2 to 3 times a week is recommended while pairing it to a moisturizer or protein treatment.</p>

