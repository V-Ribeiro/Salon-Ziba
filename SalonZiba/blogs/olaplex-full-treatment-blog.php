        <br>
               
        <h4 class="servicesTitle">What is the process of an Olaplex treatment at the salon?</h4>
        
        <br>
        
        <a href="../services/treatments.php" target="_blank">Olaplex Within Color Service</a>
        
        <br>
        
        <p>When getting your color done, Olaplex 1 will be mixed into the dye solution used in the process. Adding No. 1 Bond Multiplier will help alleviate the damage done during the color, lightening and relaxing process. But this step cannot prevent everything. This is why after all the hair color processing has been completed and rinsed, your stylist will lead you to the shampoo bowl to apply and comb Olaplex 2 through your hair, then leave it for 20 minutes or longer. This will ensure that the product finds the remaining bonds to strengthen, without the color, lightener and relaxers actively working against it. Finally, they will rinse out the product and apply shampoo and conditioner to your hair as usual.</p>
        
        <br>
        <br>
        
        <a href="../services/treatments.php" target="_blank">Olaplex Full Treatment</a>
        
        <br>
        
        <p>The Olaplex Full Treatment will find you in the trustworthy hands of one of our talented stylists at the shampoo bowl. First, your stylist will saturate your dry hair with a mix of Olaplex No. 1 and water, designed to find the broken bonds and build new connections between them, and leave it for 5 minutes. Then without rinsing, they will layer Olaplex No. 2 from your roots to your ends, on top of No. 1 Bond Multiplier, and comb through it thoroughly. This step will ensure that the previously missed broken bonds will also be targeted. They will leave the product for 20 minutes, then finally, they will rinse, shampoo and condition your hair as usual, leaving you with strengthened, shiny and soft hair.</p>
        
